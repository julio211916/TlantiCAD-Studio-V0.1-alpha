pub struct EmergencyContact {
    pub name: String,
    pub relationship: String,
    pub phone: String,
    pub phone_secondary: Option<String>,
}

/// Medical history
#[derive(Debug, Clone, Serialize, Deserialize, Default)]
pub struct MedicalHistory {
    /// Current medications
    pub medications: Vec<String>,
    
    /// Medical conditions (diabetes, hypertension, etc.)
    pub conditions: Vec<String>,
    
    /// Previous surgeries
    pub surgeries: Vec<String>,
    
    /// Blood type
    pub blood_type: Option<String>,
    
    /// Is pregnant (for female patients)
    pub pregnant: Option<bool>,
    
    /// Is nursing (for female patients)
    pub nursing: Option<bool>,
    
    /// Has heart problems
    pub heart_problems: bool,
    
    /// Has bleeding disorders
    pub bleeding_disorders: bool,
    
    /// Recent hospitalizations
    pub hospitalizations: Option<String>,
    
    /// Physician name
    pub physician_name: Option<String>,
    
    /// Physician phone
    pub physician_phone: Option<String>,
    
    /// Additional notes
    pub notes: Option<String>,
}

